package model;

public class Fila {
	
	public int id_fila;
	public String nm_fila; 
	
	// get e set
	public int getId_fila() {
		return id_fila;
	}
	
	public void setId_fila(int id_fila) {
		this.id_fila = id_fila;
	}
	
	public String getNm_fila() {
		return nm_fila;
	}
	
	public void setNm_fila(String nm_fila) {
		this.nm_fila = nm_fila;
	}
	

}
